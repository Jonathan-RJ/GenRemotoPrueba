
CREATE TABLE CLIENTES (
CLIENTES_ID NUMBER PRIMARY KEY,
NOMBRE NVARCHAR2(30),
DIRECCION NVARCHAR2(80),
FECHA_PEDIDO DATE,
FECHA_ENTREGA DATE
);

CREATE TABLE PEDIDOS (
PEDIDOS_ID NUMBER PRIMARY KEY,
CODIGO_DE_PEDIDOS NUMBER,
PRODUCTOS_ID NUMBER,
ENTREGA DATE,
PRECIO NUMBER
);

CREATE TABLE PRODUCTOS (
PRODUCTOS_ID NUMBER PRIMARY KEY,
CODIGO_PRODUCTO NVARCHAR2(50),
TAMAÑO NVARCHAR2(30)
);

CREATE TABLE PAGOS (
PAGOS_ID NUMBER PRIMARY KEY,
CLIENTES_ID NUMBER,
PRODUCTOS_ID NUMBER,
TIPO_DE_PAGO NVARCHAR2(50)
);

CREATE TABLE ENTREGAS (
ENTREGAS_ID NUMBER PRIMARY KEY,
FECHA_ENTREGA DATE,
CLIENTES_ID NUMBER,
PRODUCTOS_ID NUMBER,
DESTINATARIO NVARCHAR2(50)
);


